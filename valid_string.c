#include "push_swap.h"

int	check_char(char **argv)
{
	int	i;

	i = 0;
	while (argv[1][i])
	{
		if (argv[1][i] == 45 || argv[1][i] == 43 || argv[1][i] == 32)
			i++;
		if (argv[1][i] < 48 || argv[1][i] > 57)
			return (1);
		i++;
	}
	return (0);
}
